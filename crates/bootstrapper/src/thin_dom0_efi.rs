#[cfg(test)]
mod tests {
    use super::*;
    use crate::thin_dom0_manifest::UserDesktopDisplayMode;

    fn sample_inputs() -> GrubEntryInputs {
        GrubEntryInputs {
            entry_name: "rotten-apple ThinDom0".to_string(),
            boot_fs_uuid: "2048f8be-403d-4cb7-b79a-c821a0daa609".to_string(),
            xen_image: PathBuf::from("xen-4.20-amd64.gz"),
            dom0_kernel: PathBuf::from("rotten-apple/vmlinuz"),
            dom0_initrd: PathBuf::from("rotten-apple/thin-dom0.cpio.gz"),
            dom0_mem_mb: 4096,
            dom0_vcpus: 1,
            user_root_uuid: "9881a7d1-66a6-463d-9433-a7117badbc35".to_string(),
            dom0_pinned_cpu: Some(12),
            framebuffer_gpu_bdf: Some("0000:00:02.0".to_string()),
            display_mode: UserDesktopDisplayMode::ParavirtOnly,
            esp_fs_uuid: Some("B6D5-2FF2".to_string()),
        }
    }

    #[test]
    fn xen_cfg_has_default_and_recovery_sections() {
        let cfg = render_xen_cfg(&sample_inputs());
        assert!(cfg.contains("[global]") && cfg.contains("default=thindom0"),
                "global section must pick thindom0 as default: {cfg}");
        assert!(cfg.contains("[thindom0]") && cfg.contains("[recovery]"),
                "both sections must exist: {cfg}");
    }

    #[test]
    fn xen_cfg_sections_mirror_grub_cmdline_builders() {
        // The whole point of the shared builders: the ESP boot path and
        // the GRUB multiboot2 path carry IDENTICAL cmdlines. Pin the
        // xen.cfg lines to the builders' output verbatim.
        let g = sample_inputs();
        let cfg = render_xen_cfg(&g);
        assert!(cfg.contains(&format!("options={}", xen_options_cmdline(&g, false))));
        assert!(cfg.contains(&format!("options={}", xen_options_cmdline(&g, true))));
        assert!(cfg.contains(&format!("kernel=vmlinuz {}", dom0_kernel_cmdline(&g, false))));
        assert!(cfg.contains(&format!("kernel=vmlinuz {}", dom0_kernel_cmdline(&g, true))));
    }

    #[test]
    fn xen_cfg_has_no_multiboot_placeholder_token() {
        // "placeholder" is the multiboot first-arg convention (image
        // name slot). In xen.cfg it would be parsed as a real Xen or
        // kernel argument — unknown-param noise at best.
        let cfg = render_xen_cfg(&sample_inputs());
        assert!(!cfg.contains("placeholder"),
                "xen.cfg must not carry the multiboot placeholder token: {cfg}");
    }

    #[test]
    fn xen_cfg_recovery_section_is_observable_and_no_autostart() {
        let cfg = render_xen_cfg(&sample_inputs());
        let recovery = cfg.split("[recovery]").nth(1).expect("recovery section");
        assert!(recovery.contains("sync_console=true") && recovery.contains("noreboot"),
                "recovery options must carry sync_console + noreboot: {recovery}");
        assert!(recovery.contains("console=hvc0"),
                "recovery dom0 console must be hvc0 (visible through Xen): {recovery}");
        assert!(recovery.contains("rotten_apple.no_autostart=1"),
                "recovery must not autostart the guest: {recovery}");
    }

    #[test]
    fn xen_cfg_ramdisk_and_relative_paths() {
        // xen.efi resolves paths relative to its own directory — bare
        // filenames, no leading slash, no /boot prefix.
        let cfg = render_xen_cfg(&sample_inputs());
        assert_eq!(cfg.matches("ramdisk=initrd.cpio.gz").count(), 2,
                   "both sections need the ramdisk line: {cfg}");
        assert!(!cfg.contains("kernel=/") && !cfg.contains("ramdisk=/"),
                "ESP paths must be relative to xen.efi's dir: {cfg}");
    }

    #[test]
    fn esp_mount_parsed_from_mountinfo() {
        let info = "\
22 25 0:21 / /sys rw - sysfs sysfs rw
25 1 259:2 / / rw - ext4 /dev/nvme0n1p2 rw
40 25 259:1 / /boot/efi rw - vfat /dev/nvme0n1p1 rw
";
        assert_eq!(esp_mount_device_from_mountinfo(info),
                   Some(PathBuf::from("/dev/nvme0n1p1")));
    }

    #[test]
    fn esp_mount_none_on_bios_hosts() {
        let info = "25 1 259:2 / / rw - ext4 /dev/sda1 rw\n";
        assert_eq!(esp_mount_device_from_mountinfo(info), None);
    }

    #[test]
    fn nvram_detection_requires_label_and_loader_path() {
        // Real efibootmgr shapes from this Dell (2026-07-08).
        let with_entry = "\
BootCurrent: 0003
BootOrder: 0003,0000,0002,0005,0001
Boot0003* Ubuntu\tHD(1,GPT,0e90f3a3)/\\EFI\\ubuntu\\shimx64.efi
Boot0004* rotten-apple Xen (native EFI)\tHD(1,GPT,0e90f3a3)/\\EFI\\xen\\xen.efi
";
        assert!(nvram_has_current_entry(with_entry));
        // Same label pointing somewhere stale — must NOT count.
        let stale = "Boot0004* rotten-apple Xen (native EFI)\tHD(1,GPT,x)/\\EFI\\old\\xen.efi\n";
        assert!(!nvram_has_current_entry(stale));
        assert!(!nvram_has_current_entry("Boot0003* Ubuntu\t...\n"));
    }

    #[test]
    fn boot_order_extracted_verbatim() {
        let out = "BootCurrent: 0003\nTimeout: 5 seconds\nBootOrder: 0003,0000,0002\n";
        assert_eq!(boot_order_value(out), Some("0003,0000,0002".to_string()));
        assert_eq!(boot_order_value("no order here\n"), None);
    }

    #[test]
    fn stage_esp_writes_all_four_files() {
        let tmp = tempfile::tempdir().unwrap();
        let esp = tmp.path().join("esp");
        std::fs::create_dir_all(&esp).unwrap();
        let src = tmp.path().join("src");
        std::fs::create_dir_all(&src).unwrap();
        for (name, bytes) in [("xen.efi", b"XEN" as &[u8]),
                              ("vmlinuz", b"KRN"),
                              ("cpio",    b"RFS")] {
            std::fs::write(src.join(name), bytes).unwrap();
        }
        let inputs = EspStageInputs {
            esp_mount: esp.clone(),
            xen_efi_source: src.join("xen.efi"),
            kernel_source:  src.join("vmlinuz"),
            initrd_source:  src.join("cpio"),
        };
        stage_esp(&inputs, "cfg-body\n", false).unwrap();
        let dir = esp.join(ESP_XEN_DIR);
        assert_eq!(std::fs::read(dir.join("xen.efi")).unwrap(), b"XEN");
        assert_eq!(std::fs::read(dir.join("vmlinuz")).unwrap(), b"KRN");
        assert_eq!(std::fs::read(dir.join("initrd.cpio.gz")).unwrap(), b"RFS");
        assert_eq!(std::fs::read_to_string(dir.join("xen.cfg")).unwrap(), "cfg-body\n");
    }

    #[test]
    fn stage_esp_dry_run_touches_nothing() {
        let tmp = tempfile::tempdir().unwrap();
        let inputs = EspStageInputs {
            esp_mount: tmp.path().join("esp"),
            xen_efi_source: tmp.path().join("missing-xen.efi"),
            kernel_source:  tmp.path().join("missing-vmlinuz"),
            initrd_source:  tmp.path().join("missing-cpio"),
        };
        // Sources don't even exist — dry-run must still succeed and
        // must not create the ESP dir.
        stage_esp(&inputs, "cfg\n", true).unwrap();
        assert!(!tmp.path().join("esp").exists());
    }
}
