/// Argv to invoke mmdebstrap with. Returns a Vec so the caller can
/// `Command::new("mmdebstrap").args(&args)`. Pure — no I/O.
pub fn mmdebstrap_args(spec: &RootfsSpec) -> Vec<String> {
    let mut packages = spec.packages().iter().map(|s| s.to_string()).collect::<Vec<_>>().join(",");
    packages.insert(0, ' '); // separator
    let _ = packages.trim();
    let mut args = vec![
        "--variant=minbase".to_string(),
        format!("--include={}", spec.packages().join(",")),
        // Architecture defaults to host; mmdebstrap detects automatically.
        "--mode=auto".to_string(),
        spec.suite.clone(),
        spec.workdir.to_string_lossy().to_string(),
        spec.mirror.clone(),
    ];
    // mmdebstrap also accepts --components for non-main repos. Ubuntu's
    // xen-* lives in main + universe; include both.
    args.insert(0, "--components=main,universe".to_string());
    args
}

/// Argv to pack `workdir` into a cpio.gz at `output_cpio`. Run from
/// inside `workdir` (caller cd's into it). The `cpio` invocation is
/// gzipped via shell pipe — the executor sets up the pipe.
pub fn cpio_pack_command(workdir: &Path, output_cpio: &Path) -> String {
    // `find . | cpio -o -H newc | gzip -9` is the canonical way to
    // produce a kernel-readable initrd. -H newc = the format Linux's
    // initramfs extractor expects.
    format!(
        "cd {workdir} && find . | cpio --quiet -o -H newc | gzip -9 > {out}",
        workdir = workdir.display(),
        out = output_cpio.display(),
    )
}

// ---------------------------------------------------------------------------
// Executor — actually builds the rootfs. Not unit-tested (requires
// mmdebstrap, network, root); shipped behind install-thindom0's
// pre-flight so failures surface clearly.

#[derive(Debug)]
pub enum RootfsError {
    PreFlight(String),
    Step { step: &'static str, detail: String },
}

impl std::fmt::Display for RootfsError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        match self {
            RootfsError::PreFlight(s) => write!(f, "rootfs pre-flight: {s}"),
            RootfsError::Step { step, detail } => write!(f, "rootfs step '{step}': {detail}"),
        }
    }
}

impl std::error::Error for RootfsError {}

/// Build the dom0 cpio.gz from `spec`. Returns the cpio.gz path.
/// Creates `spec.workdir` (wiping any existing contents), invokes
/// mmdebstrap, bakes in the rotten-apple binary + /init + manifests,
/// then packs as cpio.gz. Side effects all live under `spec.workdir`
/// and `spec.output_cpio`; nothing else on the host is touched.
/// Bundle the firmware blobs the framebuffer GPU's driver needs into the
/// cpio at /lib/firmware, derived from the machine being provisioned rather
/// than a hardcoded vendor list. Asks the detected driver (i915/amdgpu/...)
/// which firmware it references via `modinfo -F firmware`, scoped to the
/// exact kernel whose modules we bundled, and copies only those files (in
/// whatever compressed variant the host ships). A headless host (no driver),
/// a builtin driver, or a missing blob is a no-op, not an error — we'd
/// rather boot than refuse to build.
fn bundle_gpu_firmware(spec: &RootfsSpec) -> Result<(), RootfsError> {
    use std::process::Command;
    use std::path::Path;
    let log = |msg: String| eprintln!("    [rootfs/firmware] {msg}");

    let driver = match spec.framebuffer_gpu_driver.as_deref() {
        Some(d) if !d.is_empty() => d,
        _ => {
            log("no framebuffer GPU driver detected — PV/headless display, \
                 skipping firmware".to_string());
            return Ok(());
        }
    };

    let dst_root = spec.workdir.join("lib/firmware");

    // Primary path: GPU drivers keep all their firmware variants under
    // /lib/firmware/<driver>/ (i915, amdgpu, nouveau all do). Copy the whole
    // subtree so we cannot miss the specific blob this panel's hardware needs.
    // modinfo's MODULE_FIRMWARE list is deliberately INCOMPLETE for i915 — it
    // declares only some DMC versions while the driver selects the matching
    // one at runtime from an internal table (observed: 18 DMC entries in
    // modinfo vs 38 on disk). cp -a preserves the compressed (.zst) form the
    // kernel decompresses at load.
    let driver_fw_dir = Path::new("/lib/firmware").join(driver);
    if driver_fw_dir.is_dir() {
        std::fs::create_dir_all(&dst_root).map_err(|e| RootfsError::Step {
            step: "mkdir /lib/firmware", detail: e.to_string(),
        })?;
        let cp = Command::new("cp").arg("-a").arg(&driver_fw_dir).arg(&dst_root).output()
            .map_err(|e| RootfsError::Step {
                step: "cp -a /lib/firmware/<driver>", detail: e.to_string(),
            })?;
        if !cp.status.success() {
            return Err(RootfsError::Step {
                step: "cp -a /lib/firmware/<driver>",
                detail: format!("exit={}\nstderr: {}", cp.status,
                    String::from_utf8_lossy(&cp.stderr)),
            });
        }
        let n = std::fs::read_dir(&driver_fw_dir).map(|d| d.count()).unwrap_or(0);
        log(format!("→ /lib/firmware/{driver} ({n} blobs, full subtree)"));
        return Ok(());
    }

    // Fallback for a driver that does NOT use a same-named firmware dir: ask
    // modinfo what it references and copy those files (with their compressed
    // variants), scoped to the kernel whose modules we bundled. Builtin /
    // firmware-less drivers yield an empty list = nothing to bundle.
    let referenced: Vec<String> = match Command::new("modinfo")
        .arg("-k").arg(&spec.kernel_release)
        .arg("-F").arg("firmware").arg(driver).output()
    {
        Ok(o) if o.status.success() => String::from_utf8_lossy(&o.stdout)
            .lines().map(str::trim).filter(|l| !l.is_empty()).map(String::from).collect(),
        _ => Vec::new(),
    };
    if referenced.is_empty() {
        log(format!("{driver}: no /lib/firmware/{driver} dir and modinfo lists \
                     no firmware — nothing to bundle"));
        return Ok(());
    }
    let fw_root = Path::new("/lib/firmware");
    let mut copied = 0usize;
    for rel in &referenced {
        let base = fw_root.join(rel);
        let candidates = [
            base.clone(),
            PathBuf::from(format!("{}.zst", base.display())),
            PathBuf::from(format!("{}.xz", base.display())),
            PathBuf::from(format!("{}.gz", base.display())),
        ];
        let Some(src) = candidates.into_iter().find(|p| p.exists()) else { continue };
        let dst_rel = src.strip_prefix(fw_root).unwrap_or_else(|_| Path::new(rel));
        let dst = dst_root.join(dst_rel);
        if let Some(parent) = dst.parent() {
            std::fs::create_dir_all(parent).map_err(|e| RootfsError::Step {
                step: "mkdir /lib/firmware subdir", detail: e.to_string(),
            })?;
        }
        if Command::new("cp").arg("-a").arg(&src).arg(&dst).output()
            .map(|o| o.status.success()).unwrap_or(false) { copied += 1; }
    }
    log(format!("→ /lib/firmware for {driver}: {copied}/{} referenced blobs (fallback)",
                referenced.len()));
    Ok(())
}

/// The kernel modules a ThinDom0 actually loads — the seeds whose full
/// dependency closure (and ONLY that closure) we bundle into the cpio,
/// instead of the entire ~190 MiB `/lib/modules/<kver>/kernel` tree. The
/// detected framebuffer GPU driver (e.g. i915/amdgpu) is appended at
/// runtime from `RootfsSpec::framebuffer_gpu_driver`.
///
/// Why each: storage (mount the host /boot for logs + see guest disks);
/// ext4 (mount_host_boot does `mount -t ext4`); the xen_* control-plane
/// set (xenstored needs gntdev/evtchn, guests need blk/netback, passthrough
/// needs pciback — see render_init_script); bridge (xenbr0); xhci (USB/dock
/// NIC enumeration); a spread of wired + USB-ethernet NIC drivers so a dock
/// or dongle brings up dom0's SSH escape hatch; virtio_* so the QEMU
/// bring-up harness ([[project_thindom0_qemu_simulator]]) still works.
/// Anything not in a seed's dep chain is simply absent — modprobe of an
/// unmatched modalias in /init fails gracefully (it's wrapped in `try`).
const DOM0_SEED_MODULES: &[&str] = &[
    // storage + root fs
    "nvme", "nvme_core", "sd_mod", "ext4",
    // xen control plane + PV backends + passthrough (must match /init)
    "xen_blkback", "xen_netback", "xenfs",
    "xen_gntdev", "xen_gntalloc", "xen_evtchn", "xen_pciback",
    // networking: bridge for xenbr0, AF_PACKET for dhclient
    "bridge", "af_packet",
    // USB host controllers (dock / USB-NIC enumeration)
    "xhci_pci", "xhci_hcd",
    // input: internal PS/2 keyboard (i8042/atkbd) + USB HID keyboards, so
    // the cockpit's VT/stdin actually gets key events. Without these the
    // kernel delivers zero keystrokes and crossterm's event::read() on tty1
    // sees nothing — a dead cockpit. i2c_hid_acpi covers newer Dell/laptop
    // internal keyboards wired over I2C instead of PS/2.
    "i8042", "atkbd", "serio_raw",
    "usbhid", "hid", "hid_generic",
    "i2c_hid", "i2c_hid_acpi",
    // wired NICs (common Intel/Realtek laptop + dock)
    "e1000e", "igc", "igb", "r8169",
    // USB-ethernet (dock dongles)
    "r8152", "cdc_ether", "usbnet", "asix", "ax88179_178a",
    // virtio (QEMU sim scratch disk + net)
    "virtio_pci", "virtio_blk", "virtio_scsi", "virtio_net",
];

/// Parse `modprobe --show-depends` for `module` under `kver` into the set of
/// absolute `.ko[.zst]` host paths it needs (the module itself + every
/// dependency, in load order). Returns empty for builtin/unknown/missing
/// modules (modprobe errors or emits only `builtin` lines) — the caller
/// tolerates that, since the seed list is deliberately broad.
fn module_dep_files(module: &str, kver: &str) -> Vec<PathBuf> {
    use std::process::Command;
    let out = Command::new("modprobe")
        .arg("--show-depends")
        .arg("--set-version").arg(kver)
        .arg(module)
        .output();
    let out = match out {
        Ok(o) if o.status.success() => o,
        _ => return Vec::new(),
    };
    String::from_utf8_lossy(&out.stdout)
        .lines()
        // lines look like: `insmod /lib/modules/<kver>/kernel/.../foo.ko.zst`
        .filter_map(|l| l.strip_prefix("insmod "))
        .filter_map(|rest| rest.split_whitespace().next())
        .map(PathBuf::from)
        .collect()
}

/// Bundle ONLY the modules a thin dom0 needs (the closure of
/// `DOM0_SEED_MODULES` + the detected GPU driver) into the cpio, then
/// regenerate modules.dep/alias against that slimmed set with `depmod -b`.
///
/// Replaces a wholesale `cp -a /lib/modules/<kver>` (~190 MiB) — which made
/// the cpio 264 MiB compressed / 461 MiB unpacked. That fat initrd was the
/// common root of two boot failures: dom0_mem unpack OOM, and GRUB's ext4
/// driver stalling for minutes on a >4-extent initrd (a 264 MiB file can't
/// hold fewer than ~3 extents and re-fragments on every write). A ~30-50
/// MiB slim initrd is a single extent, loads instantly, and unpacks in
/// well under 1 GiB. See [[project_thindom0_boot_wedge]].
fn bundle_kernel_modules(spec: &RootfsSpec) -> Result<(), RootfsError> {
    use std::process::Command;
    let log = |msg: String| eprintln!("    [rootfs/modules] {msg}");
    let kver = &spec.kernel_release;
    let host_modules = PathBuf::from(format!("/lib/modules/{kver}"));
    if !host_modules.exists() {
        return Err(RootfsError::PreFlight(format!(
            "host has no /lib/modules/{kver} — kernel-modules package missing or \
             wrong release; without it dom0 can't see NVMe/SCSI storage")));
    }

    let mut seeds: Vec<String> = DOM0_SEED_MODULES.iter().map(|s| s.to_string()).collect();
    if let Some(drv) = &spec.framebuffer_gpu_driver {
        seeds.push(drv.clone());
    }

    let mut files: BTreeSet<PathBuf> = BTreeSet::new();
    for m in &seeds {
        for f in module_dep_files(m, kver) {
            files.insert(f);
        }
    }
    // Sanity floor: if dependency resolution produced almost nothing, the
    // host tooling (modprobe/modules.dep) is broken — fail loudly rather
    // than ship a cpio with no storage drivers (silent unbootable dom0).
    if files.len() < 5 {
        return Err(RootfsError::PreFlight(format!(
            "kernel-module dependency resolution yielded only {} files for {kver}; \
             expected dozens. Is modules.dep present (run depmod)? Refusing to \
             build a cpio with no storage/xen drivers.", files.len())));
    }

    let dst_modules = spec.workdir.join(format!("lib/modules/{kver}"));
    std::fs::create_dir_all(&dst_modules).map_err(|e| RootfsError::Step {
        step: "mkdir /lib/modules/<kver>", detail: e.to_string(),
    })?;

    // Copy each resolved .ko preserving its path under the workdir. `cp -a
    // --parents <abs> <workdir>` recreates <workdir>/lib/modules/<kver>/...
    let mut cp = Command::new("cp");
    cp.arg("-a").arg("--parents");
    for f in &files {
        cp.arg(f);
    }
    cp.arg(&spec.workdir);
    let cp_out = cp.output().map_err(|e| RootfsError::Step {
        step: "cp -a --parents <modules>", detail: e.to_string(),
    })?;
    if !cp_out.status.success() {
        return Err(RootfsError::Step {
            step: "cp -a --parents <modules>",
            detail: format!("exit={}\nstderr: {}",
                cp_out.status, String::from_utf8_lossy(&cp_out.stderr)),
        });
    }

    // depmod needs the builtin/order metadata to resolve builtin symbols;
    // copy it best-effort (absent on some kernels), then regenerate
    // modules.dep + modules.alias from JUST the .ko files we copied so
    // modprobe and the /init modalias coldplug stay consistent.
    for meta in ["modules.builtin", "modules.builtin.modinfo", "modules.order"] {
        let p = host_modules.join(meta);
        if p.exists() {
            let _ = Command::new("cp").arg("-a").arg("--parents").arg(&p)
                .arg(&spec.workdir).output();
        }
    }
    let depmod = Command::new("depmod")
        .arg("-b").arg(&spec.workdir).arg(kver)
        .output()
        .map_err(|e| RootfsError::Step { step: "depmod -b", detail: e.to_string() })?;
    if !depmod.status.success() {
        return Err(RootfsError::Step {
            step: "depmod -b",
            detail: format!("exit={}\nstderr: {}",
                depmod.status, String::from_utf8_lossy(&depmod.stderr)),
        });
    }

    log(format!(
        "→ /lib/modules/{kver} (slim: {} .ko from {} seeds, vs whole-tree ~190 MiB)",
        files.len(), seeds.len()));
    Ok(())
}

pub fn build_rootfs(spec: &RootfsSpec, dry_run: bool) -> Result<PathBuf, RootfsError> {
    use std::process::Command;

    let log = |step: &str, msg: String| {
        eprintln!("    [rootfs/{step}] {msg}");
    };

    // Pre-flight: existence of the binary we'll bake in matters even
    // for dry-runs (the user wants to know if the plan is going to
    // hit a wall). Tool availability (mmdebstrap, cpio) is a real-run
    // concern only — flagging it in dry-run mode would prevent users
    // from previewing the install before they have the tools installed.
    if !spec.rotten_apple_binary.exists() {
        return Err(RootfsError::PreFlight(format!(
            "rotten-apple binary {} does not exist", spec.rotten_apple_binary.display())));
    }

    if dry_run {
        let warn = |t: &str| if which(t).is_none() {
            log("preflight", format!("warning — `{t}` not on PATH; \
                                     `apt install {t}` before --execute"));
        };
        warn("mmdebstrap");
        warn("cpio");
        log("plan", format!("would mmdebstrap {} -> {}",
                            spec.suite, spec.workdir.display()));
        log("plan", format!("would bake {} -> {}/usr/local/bin/rotten-apple",
                            spec.rotten_apple_binary.display(),
                            spec.workdir.display()));
        log("plan", format!("would pack {} -> {}",
                            spec.workdir.display(),
                            spec.output_cpio.display()));
        return Ok(spec.output_cpio.clone());
    }

    if which("mmdebstrap").is_none() {
        return Err(RootfsError::PreFlight(
            "mmdebstrap not found on PATH — install with `apt install mmdebstrap`".into()));
    }
    if which("cpio").is_none() {
        return Err(RootfsError::PreFlight(
            "cpio not found on PATH — install with `apt install cpio`".into()));
    }

    // 1. Fresh workdir.
    if spec.workdir.exists() {
        std::fs::remove_dir_all(&spec.workdir).map_err(|e| RootfsError::Step {
            step: "clean workdir",
            detail: format!("rm -rf {}: {e}", spec.workdir.display()),
        })?;
    }
    std::fs::create_dir_all(&spec.workdir).map_err(|e| RootfsError::Step {
        step: "create workdir",
        detail: format!("mkdir -p {}: {e}", spec.workdir.display()),
    })?;
    log("workdir", format!("→ {}", spec.workdir.display()));

    // 2. mmdebstrap.
    let args = mmdebstrap_args(spec);
    log("mmdebstrap", format!("mmdebstrap {}", args.join(" ")));
    let out = Command::new("mmdebstrap").args(&args).output()
        .map_err(|e| RootfsError::Step { step: "mmdebstrap spawn",
            detail: format!("{e}") })?;
    if !out.status.success() {
        return Err(RootfsError::Step {
            step: "mmdebstrap",
            detail: format!("exit={}\nstderr: {}",
                out.status, String::from_utf8_lossy(&out.stderr)),
        });
    }

    // 2.5. Bundle ONLY the kernel modules a thin dom0 needs (the closure of
    // DOM0_SEED_MODULES + the detected GPU driver), not the whole ~190 MiB
    // /lib/modules/<kver>/kernel tree. The fat tree made the cpio 264 MiB /
    // 461 MiB unpacked, which caused both the dom0_mem unpack OOM and GRUB's
    // >4-extent ext4 load stall. See bundle_kernel_modules + boot_wedge memory.
    bundle_kernel_modules(spec)?;

    // 2.6. Bundle the firmware the framebuffer GPU's driver needs — derived
    // from the machine being provisioned, NOT a hardcoded vendor matrix. The
    // driver was detected at install time (i915/amdgpu/nouveau/...); we ask
    // it which blobs it references and pull exactly those. Without the
    // display engine's firmware (e.g. i915 DMC/GuC), the driver binds but the
    // panel stays dark — the other half of the ThinDom0 black-screen story
    // (the /init `modprobe <driver>` is the first half).
    bundle_gpu_firmware(spec)?;

    // 3. Bake in rotten-apple binary at /usr/local/bin/rotten-apple.
    let dst_bin = spec.workdir.join("usr/local/bin/rotten-apple");
    if let Some(parent) = dst_bin.parent() {
        std::fs::create_dir_all(parent).map_err(|e| RootfsError::Step {
            step: "mkdir /usr/local/bin", detail: e.to_string(),
        })?;
    }
    std::fs::copy(&spec.rotten_apple_binary, &dst_bin).map_err(|e| RootfsError::Step {
        step: "copy rotten-apple",
        detail: format!("{} -> {}: {e}", spec.rotten_apple_binary.display(), dst_bin.display()),
    })?;
    set_executable(&dst_bin)?;
    log("bake", format!("→ {}", dst_bin.display()));

    // 4. /init script.
    let init = spec.workdir.join("init");
    std::fs::write(&init, render_init_script()).map_err(|e| RootfsError::Step {
        step: "write /init", detail: e.to_string(),
    })?;
    set_executable(&init)?;
    log("bake", format!("→ {}", init.display()));

    // 5. Manifests.
    let etc_dir = spec.workdir.join("etc/rotten-apple");
    std::fs::create_dir_all(&etc_dir).map_err(|e| RootfsError::Step {
        step: "mkdir /etc/rotten-apple", detail: e.to_string(),
    })?;
    std::fs::write(etc_dir.join("active.toml"), render_active_manifest_for_dom0())
        .map_err(|e| RootfsError::Step {
            step: "write active.toml", detail: e.to_string(),
        })?;
    std::fs::write(
        etc_dir.join(USER_DESKTOP_MANIFEST_FILENAME),
        &spec.user_desktop_manifest,
    )
        .map_err(|e| RootfsError::Step {
            step: "write user-desktop.toml", detail: e.to_string(),
        })?;
    std::fs::write(
        etc_dir.join(USER_DESKTOP_VISIBLE_MANIFEST_FILENAME),
        &spec.user_desktop_visible_manifest,
    )
        .map_err(|e| RootfsError::Step {
            step: "write user-desktop-visible.toml", detail: e.to_string(),
        })?;
    log("bake", format!(
        "→ {}/active.toml + {} + {}",
        etc_dir.display(),
        USER_DESKTOP_MANIFEST_FILENAME,
        USER_DESKTOP_VISIBLE_MANIFEST_FILENAME,
    ));

    // 5b. SSH authorized_keys — the user reaches dom0 from inside the
    // user-desktop guest via dropbear once iGPU passthrough makes the
    // dom0 console invisible.
    let ssh_dir = spec.workdir.join("root/.ssh");
    std::fs::create_dir_all(&ssh_dir).map_err(|e| RootfsError::Step {
        step: "mkdir /root/.ssh", detail: e.to_string(),
    })?;
    let auth = ssh_dir.join("authorized_keys");
    std::fs::write(&auth, &spec.authorized_ssh_keys).map_err(|e| RootfsError::Step {
        step: "write authorized_keys", detail: e.to_string(),
    })?;
    // chmod 600 — sshd refuses world-readable authorized_keys
    use std::os::unix::fs::PermissionsExt;
    let mut perm = std::fs::metadata(&auth).map_err(|e| RootfsError::Step {
        step: "stat authorized_keys", detail: e.to_string(),
    })?.permissions();
    perm.set_mode(0o600);
    std::fs::set_permissions(&auth, perm).map_err(|e| RootfsError::Step {
        step: "chmod authorized_keys", detail: e.to_string(),
    })?;
    if !spec.authorized_ssh_keys.is_empty() {
        let n_keys = spec.authorized_ssh_keys.lines().filter(|l| !l.trim().is_empty()).count();
        log("bake", format!("→ {} ({n_keys} SSH key(s))", auth.display()));
    } else {
        log("bake", format!("→ {} (empty — dom0 SSH disabled)", auth.display()));
    }

    // 6. Output dir for the cpio.
    if let Some(parent) = spec.output_cpio.parent() {
        std::fs::create_dir_all(parent).map_err(|e| RootfsError::Step {
            step: "mkdir output dir", detail: format!("{}: {e}", parent.display()),
        })?;
    }

    // 7. Pack as cpio.gz.
    let pack_cmd = cpio_pack_command(&spec.workdir, &spec.output_cpio);
    log("cpio", pack_cmd.clone());
    let out = Command::new("sh").args(["-c", &pack_cmd]).output()
        .map_err(|e| RootfsError::Step { step: "cpio pack spawn", detail: e.to_string() })?;
    if !out.status.success() {
        return Err(RootfsError::Step {
            step: "cpio pack",
            detail: format!("exit={}\nstderr: {}",
                out.status, String::from_utf8_lossy(&out.stderr)),
        });
    }

    let size_kb = std::fs::metadata(&spec.output_cpio)
        .map(|m| m.len() / 1024).unwrap_or(0);
    log("pack", format!("→ {} ({size_kb} KB)", spec.output_cpio.display()));

    Ok(spec.output_cpio.clone())
}

fn set_executable(path: &Path) -> Result<(), RootfsError> {
    use std::os::unix::fs::PermissionsExt;
    let mut perm = std::fs::metadata(path).map_err(|e| RootfsError::Step {
        step: "stat for chmod", detail: format!("{}: {e}", path.display()),
    })?.permissions();
    perm.set_mode(0o755);
    std::fs::set_permissions(path, perm).map_err(|e| RootfsError::Step {
        step: "chmod 755", detail: format!("{}: {e}", path.display()),
    })?;
    Ok(())
}

fn which(name: &str) -> Option<PathBuf> {
    std::env::var_os("PATH")?.to_string_lossy()
        .split(':')
        .map(|d| PathBuf::from(d).join(name))
        .find(|p| p.exists())
}

