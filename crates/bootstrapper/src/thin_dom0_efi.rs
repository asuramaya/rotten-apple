//! ThinDom0 native-EFI boot path — xen.efi on the ESP.
//!
//! WHY THIS EXISTS (2026-07-08 root cause): GRUB 2.14 release broke
//! multiboot2 on x86_64 EFI — GRUB takes a page fault inside its own
//! relocator at the final `boot` handoff, BEFORE Xen executes a single
//! instruction. Symptom: the menuentry's echo lines print, then the
//! machine freezes silently (firmware eats the #PF; laptop panels show
//! nothing). Upstream fix patches were posted to grub-devel 2026-05-13
//! (mail-archive msg43110/43111) but are merged nowhere; Ubuntu
//! resolute's 2.14-2ubuntu1/2 predates them. The 2026-05-27 unattended
//! upgrade from the working 2.14~git20250718 snapshot to the 2.14
//! release is what killed every ThinDom0 boot after May 21 — weeks of
//! kernel/cpio/dom0_mem bisection were chasing artifacts that never
//! mattered.
//!
//! The escape hatch is Xen's native EFI entry point: xen.efi is a
//! normal EFI application the firmware (or GRUB `chainloader`, which
//! uses firmware LoadImage/StartImage — no GRUB relocator) can run
//! directly. xen.efi reads a `xen.cfg` next to itself naming the dom0
//! kernel, ramdisk, and cmdlines, then boots dom0 exactly like the
//! multiboot2 path would have.
//!
//! Layout staged by `stage_esp()` (all inside the ESP, never touching
//! Ubuntu's /EFI/ubuntu — sacred rule):
//!
//!   /boot/efi/EFI/xen/xen.efi           ← copy of /boot/xen-*.efi (pkg)
//!   /boot/efi/EFI/xen/vmlinuz           ← copy of the staged dom0 kernel
//!   /boot/efi/EFI/xen/initrd.cpio.gz    ← copy of the staged dom0 rootfs
//!   /boot/efi/EFI/xen/xen.cfg           ← rendered here
//!
//! These are COPIES of /boot/rotten-apple/* — lift re-stages them on
//! every run so the ESP can't silently go stale (the manual bring-up
//! copy of 2026-07-08 had exactly that failure mode).
//!
//! xen.cfg has two sections: `thindom0` (default; autostart path) and
//! `recovery` (sync_console/noreboot Xen flags, hvc0 dom0 console,
//! rotten_apple.no_autostart=1). GRUB entries select a section by
//! passing its name as the argument after the chainloaded image path
//! (xen.efi takes the first non-option argument as the section name);
//! the raw NVRAM entry gets the default section.

use std::path::{Path, PathBuf};

use crate::thin_dom0_grub::{GrubEntryInputs, dom0_kernel_cmdline, xen_options_cmdline};

/// ESP-relative directory (also the GRUB/NVRAM path prefix, in
/// backslash form for efibootmgr).
pub const ESP_XEN_DIR: &str = "EFI/xen";
/// Label of the NVRAM boot entry we manage. Also the needle used to
/// detect an existing entry — change it and stale entries multiply.
pub const NVRAM_ENTRY_LABEL: &str = "rotten-apple Xen (native EFI)";

/// Render xen.cfg. Pure — no I/O. Cmdlines come from the SAME builders
/// as the GRUB multiboot2 entries so the two boot paths cannot drift.
///
/// Format reference: xen.efi's config parser (xen/common/efi/boot.c) —
/// INI-ish sections; `options=` is the hypervisor cmdline; `kernel=`
/// is "<filename> <dom0 cmdline...>"; `ramdisk=` a filename. Paths are
/// relative to xen.efi's own directory. No "placeholder" first token
/// anywhere — that is a multiboot convention, not an EFI one.
pub fn render_xen_cfg(g: &GrubEntryInputs) -> String {
    let xen_normal    = xen_options_cmdline(g, false);
    let xen_recovery  = xen_options_cmdline(g, true);
    let dom0_normal   = dom0_kernel_cmdline(g, false);
    let dom0_recovery = dom0_kernel_cmdline(g, true);
    format!(
        "# Generated by rotten-apple bootstrapper. Do not edit by hand — re-run\n\
         # `sudo rotten-apple lift` to regenerate. Read by xen.efi at boot.\n\
         [global]\n\
         default=thindom0\n\
         \n\
         [thindom0]\n\
         options={xen_normal}\n\
         kernel=vmlinuz {dom0_normal}\n\
         ramdisk=initrd.cpio.gz\n\
         \n\
         [recovery]\n\
         options={xen_recovery}\n\
         kernel=vmlinuz {dom0_recovery}\n\
         ramdisk=initrd.cpio.gz\n")
}

/// Walk `/proc/self/mountinfo` for the partition mounted at /boot/efi.
/// Returns `None` on BIOS hosts (no ESP mounted) — callers then skip
/// the whole native-EFI path and GRUB keeps its multiboot2 entries.
pub fn esp_mount_device_from_mountinfo(mountinfo: &str) -> Option<PathBuf> {
    for line in mountinfo.lines() {
        let mut parts = line.split(" - ");
        let pre  = parts.next().unwrap_or("");
        let post = parts.next();
        if pre.split_whitespace().nth(4) != Some("/boot/efi") {
            continue;
        }
        let post = post?;
        let mut after = post.split_whitespace();
        let _fstype = after.next()?;
        let device  = after.next()?;
        return Some(PathBuf::from(device));
    }
    None
}

/// Does efibootmgr's output already carry OUR entry pointing at OUR
/// loader path? Label match alone is not enough — a stale entry left
/// pointing somewhere else must be treated as absent (we then create a
/// fresh one; firmware tolerates duplicate labels, and the stale one is
/// reported for manual cleanup rather than auto-deleted — we never
/// delete NVRAM entries we can't prove we created).
pub fn nvram_has_current_entry(efibootmgr_output: &str) -> bool {
    efibootmgr_output.lines().any(|l|
        l.contains(NVRAM_ENTRY_LABEL) && l.contains("\\EFI\\xen\\xen.efi"))
}

/// Extract the BootOrder line's value ("0003,0000,...") so entry
/// creation can restore it verbatim — `efibootmgr -c` PREPENDS the new
/// entry to BootOrder, which would make Xen the firmware default and
/// violate the sacred rule (Ubuntu must stay the default boot).
pub fn boot_order_value(efibootmgr_output: &str) -> Option<String> {
    efibootmgr_output.lines()
        .find_map(|l| l.strip_prefix("BootOrder: "))
        .map(|s| s.trim().to_string())
}

/// Everything `stage_esp` needs. Split from ThinDom0Plan so the staging
/// logic is testable against a tempdir-shaped ESP.
#[derive(Debug, Clone)]
pub struct EspStageInputs {
    /// Mountpoint of the ESP (normally /boot/efi).
    pub esp_mount: PathBuf,
    /// Source of the hypervisor EFI image, e.g. /boot/xen-4.20-amd64.efi
    /// (dropped by xen-hypervisor-*-amd64 next to the .gz).
    pub xen_efi_source: PathBuf,
    /// The staged dom0 kernel (/boot/rotten-apple/vmlinuz) — already
    /// written by the kernel-copy step before this runs.
    pub kernel_source: PathBuf,
    /// The staged dom0 rootfs (/boot/rotten-apple/thin-dom0.cpio.gz).
    pub initrd_source: PathBuf,
}

#[derive(Debug)]
pub struct EspStageError(pub String);

impl std::fmt::Display for EspStageError {
    fn fmt(&self, f: &mut std::fmt::Formatter<'_>) -> std::fmt::Result {
        write!(f, "ESP staging failed: {}", self.0)
    }
}
impl std::error::Error for EspStageError {}

/// Copy xen.efi + dom0 kernel + dom0 rootfs onto the ESP and write the
/// rendered xen.cfg beside them. Idempotent — plain overwrites; FAT has
/// no atomic rename worth chasing and the files are only read at boot.
pub fn stage_esp(
    inputs: &EspStageInputs,
    xen_cfg: &str,
    dry_run: bool,
) -> Result<(), EspStageError> {
    let dir = inputs.esp_mount.join(ESP_XEN_DIR);
    let copies: [(&Path, &str); 3] = [
        (&inputs.xen_efi_source, "xen.efi"),
        (&inputs.kernel_source,  "vmlinuz"),
        (&inputs.initrd_source,  "initrd.cpio.gz"),
    ];
    if dry_run {
        for (src, name) in copies {
            eprintln!("    [esp] would copy {} -> {}",
                      src.display(), dir.join(name).display());
        }
        eprintln!("    [esp] would write {} ({} bytes)",
                  dir.join("xen.cfg").display(), xen_cfg.len());
        return Ok(());
    }
    std::fs::create_dir_all(&dir).map_err(|e|
        EspStageError(format!("mkdir {}: {e}", dir.display())))?;
    for (src, name) in copies {
        let dst = dir.join(name);
        std::fs::copy(src, &dst).map_err(|e| EspStageError(format!(
            "copy {} -> {}: {e}", src.display(), dst.display())))?;
        eprintln!("    [esp] {} -> {}", src.display(), dst.display());
    }
    let cfg_path = dir.join("xen.cfg");
    std::fs::write(&cfg_path, xen_cfg).map_err(|e|
        EspStageError(format!("write {}: {e}", cfg_path.display())))?;
    eprintln!("    [esp] xen.cfg -> {}", cfg_path.display());
    Ok(())
}

/// Ensure the firmware has a boot entry for \EFI\xen\xen.efi WITHOUT
/// touching BootOrder (Ubuntu stays the default; the Xen entry is
/// picked via the firmware's one-shot menu, `efibootmgr -n`, or by the
/// GRUB chainload entries which don't need NVRAM at all). Best-effort:
/// a host without efivars (chroot, container, BIOS) gets a warning,
/// never a failed lift — the GRUB chainload path works regardless.
pub fn ensure_nvram_entry(esp_disk: &Path, esp_partition: u32, dry_run: bool) {
    use std::process::Command;
    let log = |msg: String| eprintln!("    [nvram] {msg}");
    if dry_run {
        log(format!("would ensure entry {NVRAM_ENTRY_LABEL:?} -> \\EFI\\xen\\xen.efi \
                     on {} part {esp_partition} (BootOrder untouched)", esp_disk.display()));
        return;
    }
    let current = match Command::new("efibootmgr").output() {
        Ok(o) if o.status.success() => String::from_utf8_lossy(&o.stdout).to_string(),
        Ok(o) => {
            log(format!("efibootmgr exit={} — skipping NVRAM entry (GRUB \
                         chainload entries still work): {}",
                        o.status, String::from_utf8_lossy(&o.stderr).trim()));
            return;
        }
        Err(e) => {
            log(format!("efibootmgr unavailable ({e}) — skipping NVRAM entry \
                         (GRUB chainload entries still work)"));
            return;
        }
    };
    if nvram_has_current_entry(&current) {
        log(format!("entry {NVRAM_ENTRY_LABEL:?} already present — OK"));
        return;
    }
    let order_before = boot_order_value(&current);
    let created = Command::new("efibootmgr")
        .args(["-c", "-d"]).arg(esp_disk)
        .args(["-p", &esp_partition.to_string(),
               "-L", NVRAM_ENTRY_LABEL,
               "-l", "\\EFI\\xen\\xen.efi"])
        .output();
    match created {
        Ok(o) if o.status.success() => log(format!(
            "created {NVRAM_ENTRY_LABEL:?} -> \\EFI\\xen\\xen.efi")),
        Ok(o) => {
            log(format!("efibootmgr -c exit={} — entry not created: {}",
                        o.status, String::from_utf8_lossy(&o.stderr).trim()));
            return;
        }
        Err(e) => { log(format!("efibootmgr -c spawn failed: {e}")); return; }
    }
    // `-c` prepends the new entry to BootOrder — restore the pre-create
    // order so Ubuntu remains the firmware default (sacred rule). If
    // the restore fails we say so LOUDLY: the machine would now default
    // to Xen on unattended reboots.
    if let Some(order) = order_before {
        match Command::new("efibootmgr").args(["-o", &order]).output() {
            Ok(o) if o.status.success() =>
                log(format!("BootOrder restored to {order} (Ubuntu stays default)")),
            Ok(o) => log(format!(
                "WARNING: BootOrder restore FAILED (exit={}) — firmware may now \
                 default to Xen! Fix with: efibootmgr -o {order} ({})",
                o.status, String::from_utf8_lossy(&o.stderr).trim())),
            Err(e) => log(format!(
                "WARNING: BootOrder restore FAILED ({e}) — firmware may now \
                 default to Xen! Fix with: efibootmgr -o {order}")),
        }
    }
}

