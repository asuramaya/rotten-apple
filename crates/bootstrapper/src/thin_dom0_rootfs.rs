//! ThinDom0 rootfs builder.
//!
//! Produces the cpio.gz that becomes the dom0 rootfs. This file is
//! split into pure renderers (testable today, no I/O) and an executor
//! (mmdebstrap + chroot + cpio shellouts; tested by actually running
//! install-thindom0 on a real host).
//!
//! Design choice — busybox-init, not systemd:
//!
//! The dom0's only job is to bring up Xen + run the cockpit. systemd
//! drags ~80 MB of dependencies into the cpio.gz; busybox + a single
//! /init script does everything we need (mount /proc /sys /dev /run,
//! load kernel modules, start xenstored + xenconsoled, exec cockpit
//! on tty1) in ~5 MB. Smaller cpio = faster cold boot, smaller blast
//! radius for security.
//!
//! Layout of the rootfs we build:
//!
//!   /init                    busybox shell, PID 1 (this file's
//!                            `render_init_script`)
//!   /usr/local/bin/rotten-apple   the cli + cockpit binary
//!   /etc/rotten-apple/active.toml the orchestrator's manifest input
//!   /etc/rotten-apple/user-desktop.toml  baked at install time
//!   /etc/inittab             busybox-init respawn config (tty1 = cockpit)
//!   /etc/group, /etc/passwd  minimal — root only
//!   /sbin, /bin              busybox + xen-tools (xl, xenstored, ...)
//!   /lib                     libxl + glibc + helpers
//!
//! The user_root_uuid is read from /proc/cmdline at boot — that's how
//! /init knows which device to share with the user-desktop guest.

use std::collections::BTreeSet;
use std::path::{Path, PathBuf};

use crate::thin_dom0_manifest::{
    USER_DESKTOP_MANIFEST_FILENAME,
    USER_DESKTOP_VISIBLE_MANIFEST_FILENAME,
};

/// Inputs to the rootfs builder. Caller picks which manifest file
/// to bake in (the user-desktop guest's TOML) and which rotten-apple
/// binary to copy in.
#[derive(Debug, Clone)]
pub struct RootfsSpec {
    /// Path to the rotten-apple binary that becomes /usr/local/bin/rotten-apple
    /// inside the cpio. Typically `/usr/local/bin/rotten-apple` from the
    /// host (the same binary running the install).
    pub rotten_apple_binary: PathBuf,
    /// Contents of /etc/rotten-apple/user-desktop.toml inside the cpio.
    /// Generated by `thin_dom0_manifest::render_user_desktop_manifest`.
    pub user_desktop_manifest: String,
    /// Contents of /etc/rotten-apple/user-desktop-visible.toml inside
    /// the cpio. Used by the recovery GRUB entry to keep the guest on
    /// a visible paravirtual display path during passphrase bring-up.
    pub user_desktop_visible_manifest: String,
    /// Kernel driver bound to the framebuffer GPU on the host being
    /// provisioned (e.g. "i915", "amdgpu", "nouveau"), as detected at
    /// install time. Drives which firmware blobs get bundled into the cpio
    /// so dom0 can light the panel — derived from the machine, not a
    /// hardcoded vendor list. None on a headless host (no firmware bundled).
    pub framebuffer_gpu_driver: Option<String>,
    /// Authorized SSH public keys to bake into /root/.ssh/authorized_keys
    /// in the cpio. dropbear listens on xenbr0; this is how the user
    /// reaches dom0 from inside the user-desktop guest after the iGPU
    /// passes through. Empty string disables SSH access (dom0 still
    /// reachable from cockpit on tty1 before/after the guest runs,
    /// just not while it's running with iGPU).
    pub authorized_ssh_keys: String,
    /// Path on the HOST where the cpio.gz is written. Typically
    /// /boot/rotten-apple/thin-dom0.cpio.gz.
    pub output_cpio: PathBuf,
    /// Workspace directory mmdebstrap uses to stage the rootfs before
    /// packing. Removed after pack. Typically /var/cache/rotten-apple/dom0-rootfs.
    pub workdir: PathBuf,
    /// Ubuntu suite to bootstrap (jammy/noble/resolute/...). Read verbatim
    /// from the host's /etc/os-release so the dom0 rootfs is ABI-matched and
    /// carries a Xen version that actually exists in that suite's repos.
    pub suite: String,
    /// Xen major.minor (e.g. "4.20") derived from the host's boot image,
    /// used to build the versioned xen-utils-<ver>/libxenmisc<ver> package
    /// names so they match what the suite's repos actually ship.
    pub xen_version: String,
    /// `mmdebstrap` mirror URL. Default: Ubuntu archive.
    pub mirror: String,
    /// Linux kernel release string (e.g. "6.17.0-23-generic") whose
    /// `/lib/modules/<release>/` tree gets copied into the cpio after
    /// mmdebstrap. Without this, dom0 boots without ANY loadable kernel
    /// modules — a problem because critical drivers like nvme,
    /// nvme_core, and i915 are =m on Ubuntu. With no nvme module dom0
    /// can't see the host's NVMe storage at all (mount_host_boot finds
    /// nothing to scan, persistent logs are silently dropped, the user-
    /// desktop guest can't open its disk image — 2026-05-07 lesson).
    /// Caller usually plugs in `uname -r` from the host; the cpio is
    /// implicitly version-locked to the kernel copy in /boot/rotten-apple.
    pub kernel_release: String,
}

impl RootfsSpec {
    /// Apt package list. Tight on purpose — busybox + xen-tools is
    /// nearly enough; the small additions are kernel-module helpers
    /// (kmod) and a libc.
    pub fn packages(&self) -> Vec<String> {
        let mut pkgs: Vec<String> = [
            "busybox-static",        // /init shell + 200 standard utils
            "kmod",                  // modprobe, depmod for xen-blkback etc.
            "xen-utils-common",      // xenstored, xenconsoled, xl
            "iproute2",              // ip + bridge for xenbr0
            "kbd",                   // setfont (cockpit needs it on tty1)
            "ca-certificates",       // ssl trust (image pulls etc.)
            "dropbear-bin",          // tiny ssh server — dom0 reachability after iGPU passthrough
            "isc-dhcp-client",       // dhclient on xenbr0 so dom0 has an IP guests can reach
            // udev intentionally NOT included: pulls systemd transitively
            // and ThinDom0 doesn't need it. cockpit reads /sys directly
            // for GPU + IOMMU enumeration; libxl uses xenstored, not udev.
        ]
        .iter()
        .map(|s| s.to_string())
        .collect();
        // Xen runtime — package names are version-suffixed (xen-utils-4.20,
        // libxenmisc4.20 on 26.04). The version is DERIVED from the host's
        // boot image at install time (see xen_version_from_image), not
        // hardcoded, so the dom0 rootfs always matches the host's Xen across
        // upgrades instead of failing mmdebstrap on a stale version string.
        pkgs.push(format!("xen-utils-{}", self.xen_version));
        pkgs.push(format!("libxenmisc{}", self.xen_version));
        pkgs
    }
}

/// The /init script the kernel runs as PID 1 after extracting the
/// cpio.gz into tmpfs. Pure: no I/O.
///
/// Hard rules — PID 1 dying = kernel panic = boot loop:
///   * NO `set -e` / `set -u` — any non-zero return from a sub-command
///     would otherwise kill init.
///   * Every risky command goes through `try` which logs but doesn't
///     fail PID 1.
///   * A recovery busybox shell spawns on tty2 BEFORE any risky work,
///     so even if everything else goes sideways the user can switch
///     with Ctrl-Alt-F2 to debug.
///   * The cockpit on tty1 sits in a respawn loop. If cockpit can't
///     start at all (binary missing, libs missing), we drop a busybox
///     shell on tty1 instead so the user sees something.
pub fn render_init_script() -> String {
    String::from(
r#"#!/bin/busybox sh
# /init — PID 1 in ThinDom0. Kernel extracts the cpio into tmpfs and
# execs us. PID 1 dying panics the kernel and reboots, so this script
# is paranoid: no `set -e`, every risky step is logged-but-non-fatal,
# and a recovery shell is always reachable on tty2.

# Logging sinks (set before any stage() call):
#   * /dev/console — visible if a framebuffer attaches. Under Xen PV
#     dom0 without /lib/modules for i915, no fb attaches and this is
#     a black hole. Keep writing anyway in case we eventually fix it.
#   * /tmp/init.log — tmpfs, lost on reboot. Useful while /init is
#     still running and the user has a recovery shell on tty2.
#   * $PERSIST_LOG — file on the host's /boot partition (unencrypted
#     ext4 by Ubuntu default). Set after mount_host_boot succeeds.
#     Survives reboot, so we can read it from FatDom0 after a black-
#     screen ThinDom0 boot. THIS is the new diagnostic path.
PERSIST_LOG=""
TMP_LOG=/tmp/init.log

stage() {
    msg="[init] $*"
    echo "$msg" > /dev/console 2>/dev/null
    echo "$msg" >> "$TMP_LOG" 2>/dev/null
    # Persist AND flush each milestone. A late hard reset (the Xen-level
    # bootloop) loses anything still in the page cache; ext4's 5s commit
    # was the only reason earlier hang-logs survived, and an early reset
    # beats that timer. A sync per logged step is irrelevant at boot.
    if [ -n "$PERSIST_LOG" ]; then
        echo "$msg" >> "$PERSIST_LOG" 2>/dev/null
        sync 2>/dev/null
    fi
}

# try(): run a command, log status, never propagate failure to PID 1.
# On failure, captures the command's stdout+stderr into both logs so
# we can see WHY a step failed, not just THAT it failed.
try() {
    desc="$1"; shift
    out=$("$@" 2>&1)
    rc=$?
    if [ $rc -eq 0 ]; then
        stage "ok    $desc"
    else
        stage "FAIL  $desc (rc=$rc)"
        {
            echo "--- try '$desc' failed (rc=$rc) ---"
            echo "$out"
            echo "---"
        } >> "$TMP_LOG" 2>/dev/null
        if [ -n "$PERSIST_LOG" ]; then
            {
                echo "--- try '$desc' failed (rc=$rc) ---"
                echo "$out"
                echo "---"
            } >> "$PERSIST_LOG" 2>/dev/null
        fi
    fi
}

# dump_xen_logs(): snapshot the Xen control-plane logs (in tmpfs, lost on
# reboot) PLUS `xl dmesg` — the Xen HYPERVISOR console ring, i.e. the very
# messages we cannot see on this laptop's black framebuffer. Called right
# before the cockpit handoff so a black-screen boot that gets that far still
# tells us, from disk, what Xen and dom0 actually did.
dump_xen_logs() {
    [ -n "$PERSIST_LOG" ] || return 0
    {
        echo "=== xl dmesg (Xen hypervisor console ring) ==="
        xl dmesg 2>/dev/null | tail -200
        echo "=== xen control-plane logs ==="
        for f in /var/log/xen/xenstored.log /var/log/xen/xenconsoled.log \
                 /var/log/dhclient.log /var/log/dropbear.log; do
            echo "--- $f ---"
            cat "$f" 2>/dev/null
        done
        echo "=== dmesg tail at handoff ==="
        dmesg 2>/dev/null | tail -120
    } >> "$PERSIST_LOG" 2>/dev/null
    sync 2>/dev/null
}

# mount_host_boot(): scan block devices for the host's boot filesystem
# and mount it RW at /boot-host. Accept both layouts we generate:
#   - separate /boot      -> /boot-host/rotten-apple/vmlinuz
#   - /boot on /          -> /boot-host/boot/rotten-apple/vmlinuz
# Robust across hardware: no UUID lookup (busybox
# mount may not support `-U`), no udev dependency (systemd-udevd is
# NOT in the cpio package list — udev was excluded to avoid pulling
# in systemd, see RootfsSpec::packages). Best-effort: returns 1 if
# nothing matches, callers fall back to /tmp-only logging.
mount_host_boot() {
    mkdir -p /boot-host
    # NVMe namespaces can take several SECONDS to enumerate after
    # `modprobe nvme` on real hardware — the sim's virtio scratch disk is
    # instant, which masked this for the whole project. A single scan that
    # runs before /dev/nvme0n1p* exists finds nothing, returns 1, and the
    # persistent log is NEVER created — dom0 then boots blind with no
    # forensic trail. CONFIRMED-SHAPE 2026-06-15: both ThinDom0 entries
    # HALTED at black (no reboot → dom0 did NOT crash, so unpack succeeded)
    # yet left NO new init log → /init ran but mount_host_boot didn't
    # succeed. So: poll for the boot device for ~20s, and `timeout`-wrap
    # each mount so a slow/half-enumerated device can't WEDGE PID 1 here
    # (a hang would also starve the GPU coldplug below → panel stays dark).
    _wait=0
    while [ "$_wait" -lt 20 ]; do
        for dev in /dev/nvme*n*p* /dev/sd*[0-9]* /dev/mmcblk*p* /dev/vd*[0-9]* /dev/xvd*[0-9]*; do
            [ -b "$dev" ] || continue
            if timeout 5 mount -t ext4 -o ro "$dev" /boot-host 2>/dev/null; then
                if [ -f /boot-host/rotten-apple/vmlinuz ] || \
                   [ -f /boot-host/boot/rotten-apple/vmlinuz ]; then
                    mount -o remount,rw /boot-host 2>/dev/null
                    return 0
                fi
                umount /boot-host 2>/dev/null
            fi
        done
        _wait=$((_wait + 1))
        sleep 1
    done
    return 1
}

stage "rotten-apple ThinDom0 /init starting"

# busybox shipped as a single binary; install symlinks for every applet
# so /bin/sh, /bin/mount, etc. work. Idempotent.
try "busybox install symlinks"  /bin/busybox --install -s /bin

# Early mounts. /proc and /sys must come before anything reads
# /proc/cmdline. devtmpfs auto-mount from the kernel may have run
# already (our config sets CONFIG_DEVTMPFS_MOUNT) — failures here
# are tolerated.
mkdir -p /proc /sys /dev /run /tmp
try "mount /proc"  mount -t proc     proc     /proc -o nodev,noexec,nosuid
try "mount /sys"   mount -t sysfs    sysfs    /sys  -o nodev,noexec,nosuid
try "mount /dev"   mount -t devtmpfs udev     /dev  -o mode=0755,nosuid
try "mount /run"   mount -t tmpfs    tmpfs    /run  -o mode=0755,nosuid,nodev
try "mount /tmp"   mount -t tmpfs    tmpfs    /tmp  -o nosuid,nodev
# /dev/pts and /dev/shm MUST be created AFTER the devtmpfs mount above:
# devtmpfs replaces the cpio's /dev wholesale, so any pts/shm dirs made
# beforehand are shadowed and these mounts fail ENOENT (confirmed in
# init-20260522-000049.log lines 7-14). Create them on the live devtmpfs.
mkdir -p /dev/pts /dev/shm
try "mount devpts" mount -t devpts   devpts   /dev/pts -o gid=5,mode=0620
try "mount /dev/shm" mount -t tmpfs  tmpfs    /dev/shm -o nodev,nosuid

# Spawn the recovery busybox shell on tty2 RIGHT NOW — before anything
# riskier than mounting tmpfs. If the rest of this script wedges or
# panics, Ctrl-Alt-F2 reaches the shell. setsid + own session so its
# signals don't propagate back to PID 1. Order matters: this used to
# spawn after modprobe nvme + mount_host_boot, but if either of those
# hangs or panics, the user lost the escape hatch (2026-05-07 lesson).
stage "spawning recovery shell on tty2"
(setsid -c /bin/busybox sh < /dev/tty2 > /dev/tty2 2>&1) &

# Storage drivers — Ubuntu ships these as =m, so without /lib/modules
# being copied into the cpio (build_rootfs step 2.5), modprobe fails
# and dom0 has zero block devices. Critical that these run BEFORE
# mount_host_boot — without nvme.ko, /dev/nvme*n*p* never exists, the
# scan finds nothing, and the persistent log path silently drops on
# the floor. Wrapped in `timeout 10` so a hung modprobe can't wedge
# /init forever (would manifest as a bootloop — kernel reboots when
# PID 1 stalls past kernel.hung_task_timeout).
try "modprobe nvme_core" timeout 10 modprobe nvme_core
try "modprobe nvme"      timeout 10 modprobe nvme
# Nested QEMU/Xen bring-up uses a virtio scratch disk for persistent
# logs. On real hardware these are usually absent and modprobe simply
# logs FAIL without hurting boot; in the simulator they are the
# difference between proving /init reached mount_host_boot vs. seeing
# an empty /dev tree and learning nothing.
try "modprobe virtio_pci" timeout 10 modprobe virtio_pci
try "modprobe virtio_blk" timeout 10 modprobe virtio_blk
try "modprobe virtio_scsi" timeout 10 modprobe virtio_scsi
try "modprobe sd_mod"      timeout 10 modprobe sd_mod

# xen-pciback — needed to honor `xen-pciback.hide=(<bdf>)` from the
# kernel cmdline. Without this module loaded, the hide directive is
# parsed but ignored: the framebuffer GPU stays bound to dom0's
# i915/simpledrm, libxl can't claim it for the guest, and the user-
# desktop guest launches with no display (no LUKS prompt, "thin gets
# lost" — 2026-05-07). Same `timeout 10` guard as above.
try "modprobe xen_pciback" timeout 10 modprobe xen_pciback
# devtmpfs auto-creates the device nodes, but the kernel needs a beat
# to enumerate the controller and namespaces. ~1s is plenty in
# practice; we're not chasing milliseconds at boot.
sleep 1

# Persistent forensics — must come AFTER /dev is mounted AND nvme
# is loaded but BEFORE anything risky. If we black-screen later,
# this is how we find out what happened.
if mount_host_boot; then
    LOG_DIR=/boot-host/rotten-apple/log
    mkdir -p "$LOG_DIR"
    PERSIST_LOG="$LOG_DIR/init-$(date +%Y%m%d-%H%M%S 2>/dev/null || echo unknown).log"
    {
        echo "=== rotten-apple ThinDom0 /init log ==="
        echo "kernel : $(uname -r 2>/dev/null)"
        echo "cmdline: $(cat /proc/cmdline 2>/dev/null)"
        echo "uptime : $(cat /proc/uptime 2>/dev/null)"
        echo "==="
        cat "$TMP_LOG" 2>/dev/null
        echo "=== dmesg snapshot at mount time ==="
        dmesg 2>/dev/null | tail -100
        echo "=== /dev block devices ==="
        ls -la /dev/nvme* /dev/sd* /dev/mmcblk* /dev/vd* /dev/xvd* 2>/dev/null
    } >> "$PERSIST_LOG" 2>/dev/null
    stage "persistent log: $PERSIST_LOG"
else
    stage "FAIL  could not mount host /boot for persistent log"
fi

# Light the panel. THE reason ThinDom0 black-screened for weeks: this is a
# udev-less busybox PID 1, so nothing autoloads the GPU driver the way a
# normal distro's coldplug (systemd-udevd) does. Under Xen PV the EFI/
# simpledrm console starves (Xen owns the EFI GOP handoff), so the ONLY
# thing that lights the laptop panel is the native KMS driver (i915/amdgpu/
# nouveau/...) binding the framebuffer GPU. We coldplug it by hand, the
# vendor-agnostic way: walk the PCI bus and modprobe whatever driver each
# device's modalias resolves to (modules.alias from the bundled /lib/modules
# answers). The firmware that driver needs was bundled at build time.
#
# This block MUST run after mount_host_boot: loading a KMS driver under Xen
# PV is the riskiest step in this script — an i915 DMA fault can wedge the
# MACHINE, not just the modprobe process, and `timeout 10` cannot save us
# from that. The pre-attempt stage line below is synced to disk first, so a
# boot that dies right here still names the device+driver that killed it.
# (2026-06-12 lesson: this used to run before the mount; an early wedge left
# NO log and was indistinguishable from a kernel that never booted.)
#
# dom0 owns the GPU at boot (cockpit is visible on the panel); handing it to
# a passthrough guest is a later, explicit step (the Parallels-style
# takeover), NOT done here.
for _dev in /sys/bus/pci/devices/*; do
    case "$(cat "$_dev/class" 2>/dev/null)" in
        0x03*|0x02*|0x0c03*)
            # 0x03 = display controller (lights the panel — the whole point),
            # 0x02 = network controller (a built-in wired NIC, if any),
            # 0x0c03 = USB controller (so a USB/dock NIC can enumerate later).
            # All need a hand-loaded driver in this udev-less init.
            _alias="$(cat "$_dev/modalias" 2>/dev/null)"
            if [ -n "$_alias" ]; then
                stage "coldplug ${_dev##*/} starting (if this is the last line, the driver wedged the machine)"
                try "coldplug ${_dev##*/}" timeout 10 modprobe -q "$_alias"
            fi
            ;;
    esac
done

# Parse /proc/cmdline for rotten_apple.* hints. Defaults to empty.
USER_ROOT_UUID=""
DOM0_PINNED_CPU=""
HIDE_PCI=""
BREAK_STAGE=""
COCKPIT_MANIFEST="/etc/rotten-apple/user-desktop.toml"
for tok in $(cat /proc/cmdline 2>/dev/null); do
    case "$tok" in
        rotten_apple.user_root_uuid=*)
            USER_ROOT_UUID="${tok#rotten_apple.user_root_uuid=}"
            ;;
        rotten_apple.dom0_pinned_cpu=*)
            DOM0_PINNED_CPU="${tok#rotten_apple.dom0_pinned_cpu=}"
            ;;
        rotten_apple.hide_pci=*)
            HIDE_PCI="${tok#rotten_apple.hide_pci=}"
            ;;
        rotten_apple.break=*)
            BREAK_STAGE="${tok#rotten_apple.break=}"
            ;;
        rotten_apple.cockpit_manifest=*)
            COCKPIT_MANIFEST="${tok#rotten_apple.cockpit_manifest=}"
            ;;
    esac
done
[ -n "$USER_ROOT_UUID"  ] && echo "$USER_ROOT_UUID"  > /run/rotten-apple.user_root_uuid
[ -n "$DOM0_PINNED_CPU" ] && echo "$DOM0_PINNED_CPU" > /run/rotten-apple.dom0_pinned_cpu
[ -n "$HIDE_PCI" ]        && echo "$HIDE_PCI"        > /run/rotten-apple.hide_pci
[ -n "$BREAK_STAGE" ]     && echo "$BREAK_STAGE"     > /run/rotten-apple.break
echo "$COCKPIT_MANIFEST" > /run/rotten-apple.cockpit_manifest
stage "cmdline: user_root_uuid=$USER_ROOT_UUID  dom0_pinned_cpu=$DOM0_PINNED_CPU  hide_pci=$HIDE_PCI  break=$BREAK_STAGE  cockpit_manifest=$COCKPIT_MANIFEST"

maybe_break() {
    want="$1"
    [ "$BREAK_STAGE" = "$want" ] || return 0
    stage "breakpoint hit: $want"
    try "sync logs before breakpoint" sync
    while true; do
        sleep 600
    done
}

# Simulator-only breakpoint: proves /init got through early mounts,
# storage-driver loading, persistent-log setup, and cmdline parsing
# before any Xen control-plane work starts.
maybe_break before-xen

# Hand the framebuffer GPU to xen-pciback so libxl can attach it to the
# user-desktop guest at create time. Done via sysfs because the kernel-
# native cmdline syntax `xen-pciback.hide=(BDF)` panics init with
# "syntax error unexpected '('" — the parens are interpreted as shell
# metacharacters (2026-05-07 bootloop). Skip cleanly when no BDF was
# passed (headless hosts, GPU detection failed, etc.).
if [ -n "$HIDE_PCI" ]; then
    stage "binding $HIDE_PCI to xen-pciback (sysfs)"
    # Both new_slot and bind paths exist after modprobe xen_pciback.
    # If the module isn't loaded these writes silently fail (try logs
    # them) — caller sees the FAIL and looks at /var/log/init log.
    try "pciback new_slot $HIDE_PCI" \
        sh -c "echo '$HIDE_PCI' > /sys/bus/pci/drivers/pciback/new_slot"
    try "pciback bind $HIDE_PCI" \
        sh -c "echo '$HIDE_PCI' > /sys/bus/pci/drivers/pciback/bind"
fi

# Xen control plane.
mkdir -p /var/run/xen /var/log/xen /var/lib/xen
# xen-gntdev/xen-gntalloc are LOAD-BEARING for xenstored: it maps the
# xenstore ring through the grant table, so without /dev/xen/gntdev the
# daemon dies on startup with "FATAL: Failed to open connection to gnttab:
# No such file or directory" — xenstore never comes up, and cockpit then
# launches with a DEAD control plane (cannot list or start any domain).
# A udev-less busybox init autoloads nothing, so these must be explicit.
# (xenfs gives /proc/xen; blkback/netback are the PV backends for guests.)
#
# xenstored opens THREE Xen char devices at startup — privcmd (built-in),
# /dev/xen/gntdev (xen_gntdev, grant table) and /dev/xen/evtchn
# (xen_evtchn, event channel). Miss either module and it dies FATAL before
# serving a single key. xen_gntalloc pairs with gntdev for guest grants.
#
# `-a` IS LOAD-BEARING. `modprobe mod1 mod2 mod3` does NOT load three
# modules — it loads mod1 and treats mod2/mod3 as module PARAMETERS to it
# ("xen_blkback: unknown parameter 'xen_gntdev' ignored"). Without -a this
# line silently loaded ONLY xen_blkback for the life of the project;
# xen_netback/xenfs/gntdev/evtchn never loaded, so xenstored died on the
# missing grant table / event channel and cockpit ran with a dead control
# plane. Confirmed 2026-06-12 by QEMU-sim differential (one missing module
# surfaced per re-sim: gnttab → evtchn).
try "modprobe xen mods" modprobe -a xen_blkback xen_netback xenfs xen_gntdev xen_gntalloc xen_evtchn
try "mount xenfs"       mount -t xenfs xenfs /proc/xen
stage "starting xenstored"
xenstored --no-fork </dev/null >/var/log/xen/xenstored.log 2>&1 &
# Poll for xenstore readiness instead of a blind `sleep 1`. The untimed
# `xl vcpu-pin` below historically wedged PID 1 because it ran before
# xenstore could answer; wait up to ~10s for a real response.
xs_ready=0
xs_i=0
while [ $xs_i -lt 10 ]; do
    if xenstore-exists / >/dev/null 2>&1 || xenstore-read / >/dev/null 2>&1; then
        xs_ready=1; break
    fi
    sleep 1
    xs_i=$((xs_i + 1))
done
stage "xenstore ready=$xs_ready (waited ${xs_i}s)"
stage "starting xenconsoled"
xenconsoled --pid-file /run/xenconsoled.pid </dev/null >/var/log/xen/xenconsoled.log 2>&1 &

# Pin dom0 to its assigned E-core (skipped if cmdline didn't carry one).
# `timeout` guard is LOAD-BEARING: on the real laptop this xl call blocks
# indefinitely connecting to xenstore (xenstored not ready after the blind
# `sleep 1` above, or a deadlock), wedging PID 1 inside `try` right after
# "starting xenconsoled" so cockpit never launches — the black-screen
# "thindom doesn't boot". CONFIRMED 2026-05-22 by QEMU-sim differential: the
# sim omits dom0_pinned_cpu and sails past here to the cockpit handoff; the
# laptop passes dom0_pinned_cpu=N and hangs exactly here. The pin is an
# E-core optimization — never worth wedging the whole boot. Same `timeout`
# protection the modprobes above already use.
if [ -n "$DOM0_PINNED_CPU" ]; then
    try "pin dom0 vcpu" timeout 10 xl vcpu-pin Domain-0 0 "$DOM0_PINNED_CPU"
fi

# Network bridge for guests + DHCP for dom0 (so guest can SSH in).
try "create xenbr0"   ip link add name xenbr0 type bridge
try "xenbr0 up"       ip link set xenbr0 up
stage "dhclient xenbr0 (background)"
(dhclient xenbr0 -nw </dev/null >/var/log/dhclient.log 2>&1) &

# Dropbear host keys + daemon for SSH-from-guest after iGPU passthrough.
mkdir -p /etc/dropbear /root/.ssh
chmod 700 /root/.ssh 2>/dev/null
try "dropbear ed25519 host key" \
    sh -c '[ -f /etc/dropbear/dropbear_ed25519_host_key ] || \
           dropbearkey -t ed25519 -f /etc/dropbear/dropbear_ed25519_host_key'
try "dropbear ecdsa host key" \
    sh -c '[ -f /etc/dropbear/dropbear_ecdsa_host_key ] || \
           dropbearkey -t ecdsa -f /etc/dropbear/dropbear_ecdsa_host_key'
stage "starting dropbear"
(dropbear -R -s -E -p 22 </dev/null >/var/log/dropbear.log 2>&1) &

# dom0 memory governor: dom0 booted high enough to unpack the initramfs into
# RAM; hand that headroom back now by ballooning dom0 down to its steady floor,
# and let it grow again on guest/pressure demand. Backgrounded so it can never
# wedge the cockpit handoff; xenstored is already up so its `xl` calls work. A
# dead governor just means dom0 holds its boot size (safe) — so no respawn.
if [ -x /usr/local/bin/rotten-apple ]; then
    stage "starting dom0 memory governor"
    (setsid -c /usr/local/bin/rotten-apple dom0-memd >/var/log/dom0-memd.log 2>&1) &
fi

dump_xen_logs
stage "init complete — handing off to cockpit on tty1"

# Cockpit on tty1 in a respawn loop. If the cockpit binary itself is
# broken (missing libs, etc.), respawning helps nothing — fall through
# to a busybox shell on tty1 after 3 consecutive crashes within ~10s.
COCKPIT_FAILS=0
while true; do
    if [ ! -x /usr/local/bin/rotten-apple ]; then
        stage "rotten-apple binary missing — dropping busybox shell on tty1"
        setsid -c /bin/busybox sh < /dev/tty1 > /dev/tty1 2>&1
        sleep 2
        continue
    fi
    setsid -c /usr/local/bin/rotten-apple cockpit \
        --manifest "$COCKPIT_MANIFEST" \
        < /dev/tty1 > /dev/tty1 2>&1
    rc=$?
    stage "cockpit exited rc=$rc"
    COCKPIT_FAILS=$((COCKPIT_FAILS + 1))
    if [ $COCKPIT_FAILS -ge 3 ] && [ $rc -ne 0 ]; then
        stage "cockpit failed 3+ times — dropping busybox shell on tty1"
        setsid -c /bin/busybox sh < /dev/tty1 > /dev/tty1 2>&1
        COCKPIT_FAILS=0
    fi
    sleep 2
done
"#)
}

