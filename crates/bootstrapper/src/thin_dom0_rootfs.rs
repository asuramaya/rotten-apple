    #[test]
    fn cpio_pack_command_uses_newc_format_and_gzip() {
        // The kernel's initramfs extractor only understands cpio's
        // "newc" format. Plain cpio output (-H bin / no flag) silently
        // fails to extract. Pin the flag.
        let cmd = cpio_pack_command(
            Path::new("/var/cache/rotten-apple/dom0-rootfs"),
            Path::new("/boot/rotten-apple/thin-dom0.cpio.gz"),
        );
        assert!(cmd.contains("-H newc"),
                "cpio must use newc format (other formats fail to extract): {cmd}");
        assert!(cmd.contains("| gzip"),
                "output must be gzipped (multiboot2 module2 expects .gz): {cmd}");
        assert!(cmd.contains("/boot/rotten-apple/thin-dom0.cpio.gz"),
                "output path must appear in the command: {cmd}");
    }

